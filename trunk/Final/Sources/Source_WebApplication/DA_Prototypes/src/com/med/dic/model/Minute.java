package com.med.dic.model;

public class Minute {
	public int minuteId;
	public String minuteValue;
	
	public Minute() {
		
	}

	/**
	 * @return the minuteId
	 */
	public int getMinuteId() {
		return minuteId;
	}

	/**
	 * @param minuteId
	 *            the minuteId to set
	 */
	public void setMinuteId(int minuteId) {
		this.minuteId = minuteId;
	}

	/**
	 * @return the minuteValue
	 */
	public String getMinuteValue() {
		return minuteValue;
	}

	/**
	 * @param minuteValue
	 *            the minuteValue to set
	 */
	public void setMinuteValue(String minuteValue) {
		this.minuteValue = minuteValue;
	}

}
